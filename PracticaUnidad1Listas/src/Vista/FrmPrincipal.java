    public void cargarTabla(){
        if(pc != null){
            mtp.setListaPersonas(pc);
            tblListadoPersonas.setModel(mtp);
            tblListadoPersonas.updateUI();
            jScrollPane1.setVisible(true);
        }else{
            jScrollPane1.setVisible(false);
        }
    }
    
    public void agregarPersona(){
        Persona p = new Persona();
        if(txtNombres.getText().isEmpty() || txtApellidos.getText().isEmpty() || txtFechaNacimiento.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Rellene todos los campos","Error", JOptionPane.ERROR_MESSAGE);
            txtApellidos.setEnabled(true);
            txtNombres.setEnabled(true);
            txtFechaNacimiento.setEnabled(true);
            cbxGenero.setEnabled(true);
            btnAceptar.setEnabled(true);
        }else{
            p.setNombres(txtNombres.getText());
            p.setApellidos(txtApellidos.getText());
            p.setFechaNacimiento(txtFechaNacimiento.getText());
            p.setGenero(Utilidades.getGenero(cbxGenero));
            pc.calcularEdad(p);
            pc.getPersonas().insertar(p);
            limpiar();
            cargarTabla();
        }
        
    }


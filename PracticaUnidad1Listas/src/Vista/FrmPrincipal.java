    private void btnIndicacionesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIndicacionesActionPerformed
        diaPractica = new DialogPracticas(this, true);
        diaPractica.setVisible(true);
        Persona p = diaPractica.getPersona();
        if(p != null){
            try {
                pc.getPersonas().obtener(tblListadoPersonas.getSelectedRow()).setPracticas(p.getPracticas());
                cargarTabla();
                
            } catch (Exception e) {
            }
        }
        btnIndicaciones.setEnabled(false);
    }//GEN-LAST:event_btnIndicacionesActionPerformed

